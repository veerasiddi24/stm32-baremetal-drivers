void CommandProcessingTask(void *arg)
{
    CommandMessage_t cmd;
    UartMessage_t response;

    while(1)
    {
        if(xQueueReceive(CommandQueue, &cmd, portMAX_DELAY))
        {
            if(strcmp(cmd.cmd,"LED_ON")==0)
            {
                HAL_GPIO_WritePin(GPIOA,GPIO_PIN_5,GPIO_PIN_SET);
                strcpy(response.msg,"LED ON\r\n");
            }

            else if(strcmp(cmd.cmd,"LED_OFF")==0)
            {
                HAL_GPIO_WritePin(GPIOA,GPIO_PIN_5,GPIO_PIN_RESET);
                strcpy(response.msg,"LED OFF\r\n");
            }

            xQueueSend(UartWriteQueue,&response,portMAX_DELAY);
        }
    }
}

void MenuPrintTask(void *arg)
{
    UartMessage_t msg;

    while(1)
    {
        strcpy(msg.msg,
        "\r\nCommands:\r\n"
        "LED_ON\r\n"
        "LED_OFF\r\n"
        "LED_TOGGLE_START\r\n"
        "LED_TOGGLE_STOP\r\n"
        "LED_STATUS_RED\r\n"
        "LED_STATUS_STOP\r\n");

        xQueueSend(UartWriteQueue,&msg,portMAX_DELAY);

        vTaskDelay(pdMS_TO_TICKS(10000));
    }
}

void UartWriteTask(void *arg)
{
    UartMessage_t msg;

    while(1)
    {
        if(xQueueReceive(UartWriteQueue,&msg,portMAX_DELAY))
        {
            HAL_UART_Transmit(
                &huart4,
                (uint8_t*)msg.msg,
                strlen(msg.msg),
                HAL_MAX_DELAY
            );
        }
    }
}

void HAL_UART_RxCpltCallback(UART_HandleTypeDef *huart)
{
    BaseType_t xHigherPriorityTaskWoken = pdFALSE;

//    uint8_t rxChar = 0;

    if(huart->Instance == UART4)
    {
        if(rxChar != '\r')
        {
            rxBuffer[inde++] = rxChar;
        }
        else
        {
            rxBuffer[inde] = '\0';

            CommandMessage_t cmd;
            strcpy(cmd.cmd, rxBuffer);

            xQueueSendFromISR(
                CommandQueue,
                &cmd,
                &xHigherPriorityTaskWoken
            );

            inde = 0;

            portYIELD_FROM_ISR(xHigherPriorityTaskWoken);
        }

        HAL_UART_Receive_IT(&huart4, (uint8_t*)&rxChar, 1);
    }
}


